let j = -1;
let buttons = [];

function setup() {
  noCanvas();
  for (let i = 0; i < students.length; i++) {
    buttons[i] = createButton(students[i]);
    buttons[i].parent("galleryButtons");
    buttons[i].mousePressed(function() {
      j = i;
      selectProject();
    });
  }
}

function nextProject() {
  j++;
  j = j % titles.length;
  document.getElementById("frame").src = links[j];
  document.getElementById("galleryText").innerHTML = titles[j] + " by " + students[j];
}

function selectProject() {
  document.getElementById("frame").src = links[j];
  document.getElementById("qr").src = qrCodes[j];
  document.getElementById("galleryText").innerHTML = titles[j] + " by " + students[j];
  document.getElementById("description").innerHTML = descriptions[j];
}